nilai = float(input("Masukkan nilai Anda: "))

if nilai >= 90:
    print("Nilai Anda A+")
elif nilai >= 80:
    print("Nilai Anda A")
elif nilai >= 70:
    print("Nilai Anda B")
elif nilai >= 60:
    print("Nilai Anda C")
else:
    print("Anda tidak lulus.")


