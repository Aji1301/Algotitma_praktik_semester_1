# Fungsi untuk penjumlahan
def tambah(bil1, bil2):
    return bil1 + bil2

# Fungsi untuk pengurangan
def kurang(bil1, bil2):
    return bil1 - bil2

# Fungsi untuk perkalian
def kali(bil1, bil2):
    return bil1 * bil2

# Fungsi untuk pembagian
def bagi(bil1, bil2):
    if bagi != 0:
        return bil1 / bil2
    else:
        return "Pembagian oleh nol tidak diizinkan"

